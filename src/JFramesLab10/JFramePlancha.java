     private void CrearPlancha(){
        plancha = new Plancha();
        plancha.setNombre(txtNombre.getText().trim());
        plancha.setTemperatura(txtTemperatura.getText().trim());
        String selectedValue = (String) ComVapor.getSelectedItem();
    
       // Asigna el valor seleccionado a la propiedad aVapor de Plancha
         if ("SI".equals(selectedValue)) {
              plancha.setaVapor(Plancha.SI);
        } else {
            plancha.setaVapor(Plancha.NO);
        }
            this.padre.agregarNuevaPlancha(plancha);
        }
